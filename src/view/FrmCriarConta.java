    private void readTblContas() {
        DefaultTableModel modelo = (DefaultTableModel) tblContas.getModel();
        modelo.setNumRows(0);
        ContaDao dao = new ContaDao(DB.getConnection(connData));
        
        try {
            for (Conta obj : dao.findAll()) {
                modelo.addRow(new Object[] {
                    obj.getId(),
                    obj.getDescricao()
                });
            }
        }
        catch (DbException e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar contas:\n" + e.getMessage());
            System.exit(0);
        }
    }

