    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String descricao = txtDescricao.getText();
        Conta obj = new Conta(null, descricao);
        
        ContaDao dao = new ContaDao(DB.getConnection(connData));
        try {
            dao.insert(obj);
            notifyDataChangeListeners();
            JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
            readTblContas();
            txtDescricao.setText("");
        }
        catch (DbException e) {
            JOptionPane.showMessageDialog(null, "Erro ao criar conta:\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void notifyDataChangeListeners() {
        for (DataChangeListener listener : dataChangeListeners) {
            listener.onDataChanged();
        }
    }
    
    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if (tblContas.getSelectedRow() != -1) {
            Conta obj = new Conta();
            ContaDao dao = new ContaDao(DB.getConnection(connData));
            
            obj.setId((int) tblContas.getValueAt(tblContas.getSelectedRow(), 0));
            obj.setDescricao(txtDescricao.getText());
            
            try {
                dao.update(obj);
                notifyDataChangeListeners();
                JOptionPane.showMessageDialog(null, "Conta atualizada com sucesso!");
                readTblContas();
                txtDescricao.setText("");
            }
            catch (DbException e) {
                JOptionPane.showMessageDialog(null, "Erro ao atualizar conta:\n" + e.getMessage());
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Nenhuma conta selecionada!");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void tblContasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblContasMouseClicked
        if (tblContas.getSelectedRow() != -1) {
            txtDescricao.setText(tblContas.getValueAt(tblContas.getSelectedRow(), 1).toString());
        }
    }//GEN-LAST:event_tblContasMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (tblContas.getSelectedRow() != -1) {
            int id = (int) tblContas.getValueAt(tblContas.getSelectedRow(), 0);
            ContaDao dao = new ContaDao(DB.getConnection(connData));
            
            if (!(dao.hasExistingMovements(id))) {
                try {
                    dao.deleteById(id);
                    notifyDataChangeListeners();
                    JOptionPane.showMessageDialog(null, "Conta excluída com sucesso!");
                    readTblContas();
                    txtDescricao.setText("");
                }
                catch (DbException e) {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir conta:\n" + e.getMessage());
                }
            }
            else {
                int response = JOptionPane.showConfirmDialog(null, "Esta conta possui transações.\nAo exclui-la, você excluirá, também, essas transações.\nDeseja continuar?", "Excluir conta?", JOptionPane.YES_NO_OPTION);
                
                if (response == JOptionPane.YES_OPTION) {
                    TransacaoDao transacaoDao = new TransacaoDao(DB.getConnection(connData));
                    
                    try {
                        transacaoDao.deleteByContaId(id);
                        dao.deleteById(id);
                        notifyDataChangeListeners();
                        JOptionPane.showMessageDialog(null, "Conta e transações excluídas com sucesso!");
                        readTblContas();
                        txtDescricao.setText("");
                    }
                    catch (DbException e) {
                        JOptionPane.showMessageDialog(null, "Erro ao excluir conta:\n" + e.getMessage());
                    }
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Nenhuma conta selecionada!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

